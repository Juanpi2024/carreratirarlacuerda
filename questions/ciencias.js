    generateQuestion: function (level, difficulty) {
        const bank = this._banks[level];
        if (!bank || bank.length === 0) {
            return { text: 'Sin preguntas para este nivel', answer: 1, options: ['—','—','—','—'] };
        }
        // Difficulty filtering: easy=first third, normal=middle, hard=last third
        let pool = bank;
        if (difficulty === 1) pool = bank.slice(0, Math.ceil(bank.length * 0.4));
        else if (difficulty === 3) pool = bank.slice(Math.floor(bank.length * 0.6));
        const q = pool[Math.floor(Math.random() * pool.length)];
        return { text: q.text, answer: q.answer, options: q.options };
    }
